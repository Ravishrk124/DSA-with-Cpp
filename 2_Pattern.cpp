#include<iostream>   // Include input-output stream
using namespace std; // Use standard namespace

int main() {
    // Print a right-angled triangle pattern using \n for new lines
    cout << "****\n";  // First line with 4 stars
    cout << "***\n";   // Second line with 3 stars
    cout << "**\n";    // Third line with 2 stars
    cout << "*";       // Fourth line with 1 star (no \n at end)
    
    return 0; // Successful execution
}