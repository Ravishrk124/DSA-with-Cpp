#include<iostream>
using namespace std;

int main() {
    // Define rectangle dimensions
    int length = 5;
    int breadth = 3;

    // Print dimensions
    cout << "Length: " << length << endl;
    cout << "Breadth: " << breadth << endl;

    // Calculate and print area
    int area = length * breadth;
    cout << "Area of rectangle = " << area << endl;

    return 0; // program ends successfully
}